class AliceGame {
    public:
    long long findMinimumValue(long long x, long long y) {
        ll s = x+y;

        ll l=0, r=2000000;
        while(r-l>1)
        {
            ll m = (l+r)/2;
            if(m*m > s) r=m;
            else l=m;
        }

        if(l*l != s) return -1;

        if(x==0) return 0;

        ll ret=0;
        for(ll i=l; i>0; --i)
        {
            ll p = 2*i-1;
            if(x-p>=0 && x-p!=2)
            {
                x-=p;
                ++ret;
            }
            else y-=p;
        }

        if(x==0 && y==0) return ret;
        return -1;
    }
};

